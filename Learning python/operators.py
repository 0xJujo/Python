num1=50
num2=3

#arithmetic operators
print("num1 + num2",num1+num2)
print("num1 - num2",num1-num2)
print("num1 * num2",num1*num2)
print("num1 / num2",num1/num2)
print("num1 // num2",num1//num2)
print("num1 ** num2",num1**num2)
print("num1 % num2",num1%num2)

#comparison operators

print (num1==num2)
print (num1!=num2)
print (num1>num2)
print (num1<num2)

#Assignment operators
num1+=num2
print(num1)

#logical operators
print(num1>num2 and num1<num2)
print(num1>num2 or num1<num2)
print(not(num1>num2 and num1<num2))
